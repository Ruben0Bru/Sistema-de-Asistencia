    private void cargarEventosEnComboBox() {
    java.awt.EventQueue.invokeLater(() -> {
        try (Connection con = ConexionDB.getConnection()) {
            String query = "SELECT nombre_evento FROM eventos";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            eventoComboBox.removeAllItems(); // Limpiar items previos

            while (rs.next()) {
                eventoComboBox.addItem(rs.getString("nombre_evento"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar eventos: " + ex.getMessage());
        }
    });
}

